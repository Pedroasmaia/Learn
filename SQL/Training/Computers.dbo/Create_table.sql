Create table computer

( 
    id_computer int primary key, /*primary key é um identificador unico, não podem existir dois itens com o mesmo valor nesta tabela*/
    marca varchar(15), /*varchar é o tamanho máximo que uma variavel deve ter*/
    country varchar(2),  /*varchar é o tamanho máximo que uma variavel deve ter*/
    memory int /*Int define o tipo de dado*/

)